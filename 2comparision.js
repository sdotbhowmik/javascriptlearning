// Comparison operators are used in logical statements to check for example - if one variable is equal to another variable or not

// == equal value
var a = 10
var b = 5
var c = '10'

console.log(a==b)
console.log(a==c)


// === Equal value and Equal type
var a = 10 // Number
var b = 5
var c = '10' // String

console.log(a===b)
console.log(a===c)

// != not equal value

var a = 10
var b = 5
var c = '10'

console.log(a!=b)
console.log(a!=c)


// !== NOT Equal value and NOT Equal type
var a = 10 // Number
var b = 5
var c = '10' // String

console.log(a!==b)
console.log(a!==c)

// greater than
console.log(5 > 10)

// less than
console.log(15 < 10)

// greater than or equal to
console.log(10 >= 10)

// less than or equal to
console.log(11 <= 10)